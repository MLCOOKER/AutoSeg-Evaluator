# ---- Regression tests for the dictionary-substitution distortion bug ----
#
# v2 originally substituted the TG-263 canonical into the fuzzy comparison.
# When only one side resolved via the dictionary, this produced:
#   • short canonical vs verbose unresolved name → unfair length mismatch
#   • case-preserved canonical vs lowercase fallback → broken cosine overlap
# The fix uses the cleaned-but-not-substituted raw forms for fuzzy. These
# tests pin the v1-like behaviour back in place against the four real-world
# regressions reported on the SAMPLE DATA cohort.


def _best_of(synonyms_flat, gt: str, candidates: list[str]) -> str:
    """Return the candidate that scores highest against ``gt``."""
    scored = [(c, similarity(gt, c, synonyms_flat=synonyms_flat).score) for c in candidates]
    scored.sort(key=lambda x: x[1], reverse=True)
    return scored[0][0]


def test_regression_eye_l_picks_eye_globe_over_kidney(synonyms_flat):
    """Eye_L vs candidates including the description-resolved 'Kidney Left'
    and the unresolved 'Eye Globe Left'. The verbose-but-relevant candidate
    must win over the unrelated short canonical."""
    chosen = _best_of(synonyms_flat, "Eye_L", ["Eye Globe Left", "Kidney Left"])
    assert chosen == "Eye Globe Left"


def test_regression_lens_l_picks_lens_eye_over_lung(synonyms_flat):
    """lens_L vs Lung_L (short, character-similar canonical) and Lens_Eye_L
    (unresolved verbose). The semantically-correct Lens_Eye_L must win."""
    chosen = _best_of(synonyms_flat, "lens_L", ["Lens_Eye_L", "Lung_L"])
    assert chosen == "Lens_Eye_L"


def test_regression_eye_r_picks_matching_side_experimental(synonyms_flat):
    """eye_R vs eye_l_experimental and eye_r_experimental. With consistent
    case handling, the R-matching candidate wins via the single-char cosine
    advantage. Previously the canonical's uppercase R didn't match the
    lowercase r in the candidates."""
    chosen = _best_of(synonyms_flat, "eye_R", ["eye_l_experimental", "eye_r_experimental"])
    assert chosen == "eye_r_experimental"


def test_regression_submang_l_picks_glnd_submand(synonyms_flat):
    """SubmanG_L (typo, doesn't resolve) vs Glnd_Submand_L (resolves) and
    Buccal_Mucosa_L (unresolved). The semantically-closer Glnd_Submand_L
    must win — previously the case mismatch from preserving canonical
    casing handicapped it."""
    chosen = _best_of(synonyms_flat, "SubmanG_L", ["Glnd_Submand_L", "Buccal_Mucosa_L"])
    assert chosen == "Glnd_Submand_L"


# ---- Method labelling ----------------------------------------------------


def test_unresolved_names_fall_back_to_fuzzy(synonyms_flat):
    """Names not in the dictionary still match each other — via fuzzy."""
    m = similarity("CustomROIName", "CustomROIName", synonyms_flat=synonyms_flat)
    assert m.score == 1.0
    assert m.method == "fuzzy"  # literal equality, not dictionary-backed


def test_partial_match_method_is_fuzzy(synonyms_flat):
    """High-similarity but not dictionary-grounded → fuzzy."""
    m = similarity("MyOrgan_LeftSide", "MyOrgan_Left", synonyms_flat=synonyms_flat)
    assert m.method == "fuzzy"
    assert m.score < 1.0
