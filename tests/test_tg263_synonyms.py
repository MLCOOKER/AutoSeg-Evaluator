"""TG-263 dictionary round-trip tests.

Loads the bundled ``synonyms.json`` and asserts that the bridges we care
about (manual ↔ autocontour) resolve to a single TG-263 canonical, and
that the pitfalls (laterality, semantic-distinct overlapping names) do
NOT collapse.

The dictionary is generated by ``scripts/build_synonyms.py`` from the
official TG-263 worksheet. If this test file fails, either the generator
changed its expansion rules or someone hand-edited ``synonyms.json``
breaking a documented behaviour — either way, an audit is needed before
merging.
"""

from __future__ import annotations

from importlib.resources import files

import pytest

from autoseg_evaluator.core.matching import similarity
from autoseg_evaluator.data.synonyms import flatten_synonyms, load_synonyms


@pytest.fixture(scope="module")
def synonyms_flat():
    """Load the bundled TG-263 dictionary, flattened for the matcher."""
    res = files("autoseg_evaluator.resources").joinpath("synonyms.json")
    raw = load_synonyms(str(res))
    return flatten_synonyms(raw)


def _assert_tg263(synonyms_flat, a: str, b: str) -> None:
    """Both names must resolve to the same TG-263 canonical (score 1.0, method tg263)."""
    m = similarity(a, b, synonyms_flat=synonyms_flat)
    assert m.score == 1.0, f"Expected {a!r} ↔ {b!r} to score 1.0, got {m.score:.3f}"
    assert m.method == "tg263", (
        f"Expected {a!r} ↔ {b!r} to be TG-263 grounded, got method={m.method!r}"
    )


def _assert_not_collapsed(synonyms_flat, a: str, b: str, *, score_under: float = 1.0) -> None:
    """Two names must not be treated as identical (laterality / semantic distinctness)."""
    m = similarity(a, b, synonyms_flat=synonyms_flat)
    assert m.score < score_under, (
        f"Expected {a!r} ↔ {b!r} to NOT collapse — got {m.score:.3f}"
    )


# ---- Dictionary smoke-tests ---------------------------------------------


def test_dictionary_loaded_with_expected_scale(synonyms_flat):
    """The dictionary should ship with the TG-263 derived ~600 canonicals."""
    canonicals = set(synonyms_flat.values())
    # Loose floor — protects against accidentally shipping the 17-entry starter.
    assert len(canonicals) > 500, (
        f"Expected ≥500 TG-263 canonicals, found {len(canonicals)}. "
        "Did the dictionary regress to the starter file?"
    )


