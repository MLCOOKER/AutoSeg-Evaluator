# 3D and 2D neighbourhood-encoding kernels (used by ndimage.correlate).
ENCODE_NEIGHBOURHOOD_3D_KERNEL = np.array([[[128, 64], [32, 16]], [[8, 4], [2, 1]]])
ENCODE_NEIGHBOURHOOD_2D_KERNEL = np.array([[8, 4], [2, 1]])


def create_table_neighbour_code_to_surface_area(spacing_mm) -> np.ndarray:
    """256-element lookup of neighbourhood code → surface area for given spacing."""
    out = np.zeros(256)
    for code in range(256):
        normals = np.array(_NEIGHBOUR_CODE_TO_NORMALS[code])
        sum_area = 0.0
        for k in range(normals.shape[0]):
            n = np.zeros(3)
            n[0] = normals[k, 0] * spacing_mm[1] * spacing_mm[2]
            n[1] = normals[k, 1] * spacing_mm[0] * spacing_mm[2]
            n[2] = normals[k, 2] * spacing_mm[0] * spacing_mm[1]
            sum_area += float(np.linalg.norm(n))
        out[code] = sum_area
    return out


def create_table_neighbour_code_to_contour_length(spacing_mm) -> np.ndarray:
    """16-element 2D lookup mapping 4-bit codes to contour-segment lengths."""
    arr = np.zeros(16)
    vertical = spacing_mm[0]
    horizontal = spacing_mm[1]
    diag = 0.5 * math.sqrt(spacing_mm[0] ** 2 + spacing_mm[1] ** 2)
    arr[int("0001", 2)] = diag
    arr[int("0010", 2)] = diag
    arr[int("0011", 2)] = horizontal
    arr[int("0100", 2)] = diag
    arr[int("0101", 2)] = vertical
    arr[int("0110", 2)] = 2 * diag
    arr[int("0111", 2)] = diag
    arr[int("1000", 2)] = diag
    arr[int("1001", 2)] = 2 * diag
    arr[int("1010", 2)] = vertical
    arr[int("1011", 2)] = diag
    arr[int("1100", 2)] = horizontal
    arr[int("1101", 2)] = diag
    arr[int("1110", 2)] = diag
    return arr


def _compute_bounding_box(mask: np.ndarray):
    num_dims = len(mask.shape)
    bbox_min = np.zeros(num_dims, np.int64)
    bbox_max = np.zeros(num_dims, np.int64)
    proj_0 = np.amax(mask, axis=tuple(range(num_dims))[1:])
    idx_nz_0 = np.nonzero(proj_0)[0]
    if len(idx_nz_0) == 0:
        return None, None
    bbox_min[0] = np.min(idx_nz_0)
    bbox_max[0] = np.max(idx_nz_0)
    for axis in range(1, num_dims):
        max_axes = list(range(num_dims))
        max_axes.pop(axis)
        proj = np.amax(mask, axis=tuple(max_axes))
        idx_nz = np.nonzero(proj)[0]
        bbox_min[axis] = np.min(idx_nz)
        bbox_max[axis] = np.max(idx_nz)
    return bbox_min, bbox_max


def _crop_to_bounding_box(mask: np.ndarray, bbox_min: np.ndarray, bbox_max: np.ndarray) -> np.ndarray:
    """Crop a mask to ``bbox_min..bbox_max`` with a 1-voxel buffer on the max side."""
    cropmask = np.zeros((bbox_max - bbox_min) + 2, np.uint8)
    num_dims = len(mask.shape)
    if num_dims == 2:
        cropmask[0:-1, 0:-1] = mask[bbox_min[0]:bbox_max[0] + 1, bbox_min[1]:bbox_max[1] + 1]
    elif num_dims == 3:
        cropmask[0:-1, 0:-1, 0:-1] = mask[
            bbox_min[0]:bbox_max[0] + 1,
            bbox_min[1]:bbox_max[1] + 1,
            bbox_min[2]:bbox_max[2] + 1,
        ]
    return cropmask


def _sort_distances_surfels(distances: np.ndarray, surfel_areas: np.ndarray):
    # Default tuple-lex sort matches the google-deepmind implementation exactly
    # — tied distances are then sorted by surfel area.
    sorted_surfels = np.array(sorted(zip(distances, surfel_areas)))
    return sorted_surfels[:, 0], sorted_surfels[:, 1]


def compute_surface_distances(mask_gt: np.ndarray, mask_pred: np.ndarray, spacing_mm) -> dict:
    """Compute symmetric surface distances and their associated surfel areas.

    Returns a dict with keys ``distances_gt_to_pred``, ``distances_pred_to_gt``,
    ``surfel_areas_gt`` and ``surfel_areas_pred`` (each a numpy array).
    """
    if not (len(mask_gt.shape) == len(mask_pred.shape) == len(spacing_mm)):
        raise ValueError("Mask dimensions and spacing dimensions do not match.")
    num_dims = len(spacing_mm)
    if num_dims == 2:
        arr_surface_area = create_table_neighbour_code_to_contour_length(spacing_mm)
        kernel = ENCODE_NEIGHBOURHOOD_2D_KERNEL
        full_true_neighbours = 0b1111
    elif num_dims == 3:
        arr_surface_area = create_table_neighbour_code_to_surface_area(spacing_mm)
        kernel = ENCODE_NEIGHBOURHOOD_3D_KERNEL
        full_true_neighbours = 0b11111111
    else:
        raise ValueError("Only 2D or 3D masks are supported.")

    bbox_min, bbox_max = _compute_bounding_box(mask_gt | mask_pred)
    if bbox_min is None:
        return dict(distances_gt_to_pred=np.array([]), distances_pred_to_gt=np.array([]),
                    surfel_areas_gt=np.array([]), surfel_areas_pred=np.array([]))

    cropmask_gt = _crop_to_bounding_box(mask_gt, bbox_min, bbox_max)
    cropmask_pred = _crop_to_bounding_box(mask_pred, bbox_min, bbox_max)

    neighbour_code_map_gt = ndimage.correlate(
        cropmask_gt.astype(np.uint8), kernel, mode="constant", cval=0
    )
    neighbour_code_map_pred = ndimage.correlate(
        cropmask_pred.astype(np.uint8), kernel, mode="constant", cval=0
    )

    borders_gt = (neighbour_code_map_gt != 0) & (neighbour_code_map_gt != full_true_neighbours)
    borders_pred = (neighbour_code_map_pred != 0) & (neighbour_code_map_pred != full_true_neighbours)

    if borders_gt.any():
        distmap_gt = ndimage.distance_transform_edt(~borders_gt, sampling=spacing_mm)
    else:
        distmap_gt = np.inf * np.ones(borders_gt.shape)
    if borders_pred.any():
        distmap_pred = ndimage.distance_transform_edt(~borders_pred, sampling=spacing_mm)
    else:
        distmap_pred = np.inf * np.ones(borders_pred.shape)

    surface_area_map_gt = arr_surface_area[neighbour_code_map_gt]
    surface_area_map_pred = arr_surface_area[neighbour_code_map_pred]
    distances_gt_to_pred = distmap_pred[borders_gt]
    distances_pred_to_gt = distmap_gt[borders_pred]
    surfel_areas_gt = surface_area_map_gt[borders_gt]
    surfel_areas_pred = surface_area_map_pred[borders_pred]
    if distances_gt_to_pred.shape != (0,):
        distances_gt_to_pred, surfel_areas_gt = _sort_distances_surfels(
            distances_gt_to_pred, surfel_areas_gt
        )
    if distances_pred_to_gt.shape != (0,):
        distances_pred_to_gt, surfel_areas_pred = _sort_distances_surfels(
            distances_pred_to_gt, surfel_areas_pred
        )
    return dict(
        distances_gt_to_pred=distances_gt_to_pred,
        distances_pred_to_gt=distances_pred_to_gt,
        surfel_areas_gt=surfel_areas_gt,
        surfel_areas_pred=surfel_areas_pred,
    )


def compute_average_surface_distance(surface_distances: dict) -> tuple[float, float]:
    """Symmetric mean surface distance — returns ``(gt→pred, pred→gt)``."""
    d12 = surface_distances["distances_gt_to_pred"]
    d21 = surface_distances["distances_pred_to_gt"]
    a1 = surface_distances["surfel_areas_gt"]
    a2 = surface_distances["surfel_areas_pred"]
    avg12 = np.sum(d12 * a1) / np.sum(a1) if np.sum(a1) > 0 else math.nan
    avg21 = np.sum(d21 * a2) / np.sum(a2) if np.sum(a2) > 0 else math.nan
    return (avg12, avg21)


def compute_robust_hausdorff(surface_distances: dict, percent: float) -> float:
    """Robust (percentile-based) symmetric Hausdorff distance."""
    d12 = surface_distances["distances_gt_to_pred"]
    d21 = surface_distances["distances_pred_to_gt"]
    a1 = surface_distances["surfel_areas_gt"]
    a2 = surface_distances["surfel_areas_pred"]
    if len(d12) > 0:
        cumsum1 = np.cumsum(a1) / np.sum(a1)
        idx1 = np.searchsorted(cumsum1, percent / 100.0)
        val1 = d12[min(idx1, len(d12) - 1)]
    else:
        val1 = np.inf
    if len(d21) > 0:
        cumsum2 = np.cumsum(a2) / np.sum(a2)
        idx2 = np.searchsorted(cumsum2, percent / 100.0)
        val2 = d21[min(idx2, len(d21) - 1)]
    else:
        val2 = np.inf
    return float(max(val1, val2))


def compute_surface_dice_at_tolerance(surface_distances: dict, tolerance_mm: float) -> float:
    """Surface Dice coefficient at a given mm tolerance (returns 0..1 or NaN)."""
    d12 = surface_distances["distances_gt_to_pred"]
    d21 = surface_distances["distances_pred_to_gt"]
    a1 = surface_distances["surfel_areas_gt"]
    a2 = surface_distances["surfel_areas_pred"]
    overlap_gt = np.sum(a1[d12 <= tolerance_mm]) if np.sum(a1) > 0 else 0.0
    overlap_pred = np.sum(a2[d21 <= tolerance_mm]) if np.sum(a2) > 0 else 0.0
    denom = np.sum(a1) + np.sum(a2)
    if denom == 0:
        return math.nan
    return float((overlap_gt + overlap_pred) / denom)


def compute_dice_coefficient(mask_gt: np.ndarray, mask_pred: np.ndarray) -> float:
    """Volumetric Dice coefficient, or NaN when both masks are empty."""
    sum_vol = int(mask_gt.sum() + mask_pred.sum())
    if sum_vol == 0:
        return math.nan
    inter = int((mask_gt & mask_pred).sum())
    return float(2 * inter / sum_vol)
